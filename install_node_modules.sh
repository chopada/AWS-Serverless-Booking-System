#!/bin/bash

# List of folders
folders=(
  "book-ack-function"
  "cancel-ack-function"
  "book-payment-function"
  "cancel-payment-function"
  "book-entry-function"
  "cancel-entry-function"
  "notification-function"
  "booked-ticket-function"
)

# Loop through each folder
for folder in "${folders[@]}"; do
  # Check if node_modules folder is missing
  if [ ! -d "$folder/node_modules" ]; then
    # Run npm install
    echo "Running 'npm i' in $folder"
    (cd "$folder" && npm i)
  else
    echo "Node modules already installed in $folder"
  fi
done
